import 'package:flutter/material.dart';
import 'package:seedeal01/models/AppConfigModel.dart';

//==========================================================================
// MAIN CLASS
//==========================================================================
class RadioHoriGroup extends StatefulWidget {
  @override
  RadioHoriGroupWidget createState() => RadioHoriGroupWidget();
}

//==========================================================================
// STATE WIDGET
//==========================================================================
class RadioHoriGroupWidget extends State {
//==========================================================================
// DEFAULT VARIABLE
//==========================================================================
  int value = 1;
  List<ItemList> itemList = [
    ItemList(
      index: 1,
      name: "Male",
    ),
    ItemList(
      index: 2,
      name: "Female",
    ),
  ];

//==========================================================================
// BUILD WIDGET
//==========================================================================
  build(BuildContext context) {
    return Row(
      children:
//==========================================================================
// LOOP TO SHOW IN COLUMN
//==========================================================================
          itemList.map((data) => Row(children: <Widget>[
//==========================================================================
// RADIO BUTTON
//==========================================================================                      
                      Radio(
                        groupValue: value,
                        value: data.index,
                        onChanged: (val) {setState(() {value = data.index;});},
                      ),
//==========================================================================
// TEXT
//==========================================================================                      
                      Text("${data.name}",style: AppConfigModel().textStyleM,),                      
                    ],
                  )
                ).toList(),
    );
  }
}

//==========================================================================
// MODEL: ITEM LIST
//==========================================================================
class ItemList {
  String name;
  int index;
  ItemList({this.name, this.index});
}
